// While & Do While Loop

console.log("---------------While--------------");

// Mendefinisikan while loop 
let w = 0;
while(w < 16){
    w++;
    console.log('Bagas');
}

console.log("-------------Do While-------------");

// Mendefinisikan do while loop 
let d = 0
do{
    d++;
    console.log('Naqi');
}while(d < 19);




