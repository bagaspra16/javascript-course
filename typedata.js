// Variabel dengan let & macam-macam tipe data
console.log("---------String---------");

let nama = "Bagas";
console.log("First Data =",nama);
console.log("Type of Data =",typeof nama);

nama = "Pratama";
console.log("Type of Data =",typeof nama);
console.log("Second Data =",nama);

console.log("---------Number---------");

let umur = 18;
console.log("First Data =",umur);
console.log("Type of Data =",typeof umur);

let umurTambah = umur + 1;
console.log("Type of Data =",typeof umurTambah);
console.log("Second Data =",umurTambah);

console.log("---------Boolean---------");

let pilihan = false;
console.log("First Data =",pilihan);
console.log("Type of Data =",typeof pilihan); 

pilihan = true;
console.log("Type of Data =",typeof pilihan);    
console.log("Second Data =",pilihan);

console.log("---------Undefined---------");

let kosong;
console.log("First Data =",kosong);
console.log("Type of Data =",typeof kosong);

kosong = "keisi";
console.log("Type of Data =",typeof kosong);    
console.log("Second Data =",kosong);
